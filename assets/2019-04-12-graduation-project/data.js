function textToCollection(text) {
  return text.split('\n').map(line => {
    let items = line.split('	');
    return {
      T: parseFloat(items[0]),
      p: parseFloat(items[1]) * 1e6,
      rho: parseFloat(items[2]),
      i: parseFloat(items[3]) * 1e3,
      s: parseFloat(items[4]) * 1e3,
      Z: parseFloat(items[5]),
      eta: items.length >= 6 ? parseFloat(items[6]) : 0 // Viscosity
    };
  });
}

function collectionToMap(collection, keyA, keyB) {
  let result = {};
  for (var i = 0; i < collection.length; i++) {
    if (result[collection[i][keyA]] == undefined) {
      result[collection[i][keyA]] = {};
    }
    result[collection[i][keyA]][collection[i][keyB]] = collection[i];
  }
  return result;
}

function calcRect(a, b, c, d, ra, rb, rc, rd) {
  var result = {};
  for (var key in a) {
    result[key] = a[key] * ra + b[key] * rb + c[key] * rc + d[key] * rd;
  }
  return result;
}

function calcByTwoValues(map, valueA, valueB) {
  let minI, maxI, minJ, maxJ;
  for (let keyA in map) {
    if (parseFloat(keyA) < valueA) {
      if (minI === undefined || minI < parseFloat(keyA)) {
        minI = keyA;
      }
    } else {
      if (maxI === undefined || maxI > parseFloat(keyA)) {
        maxI = keyA;
      }
    }
  }
  for (let keyB in map[minI]) {
    if (parseFloat(keyB) < valueB) {
      if (minJ === undefined || minJ < parseFloat(keyB)) {
        minJ = keyB;
      }
    } else {
      if (maxJ === undefined || maxJ > parseFloat(keyB)) {
        maxJ = keyB;
      }
    }
  }
  let ratioA = (valueA - minI) / (maxI - minI);
  let ratioB = (valueB - minJ) / (maxJ - minJ);
  return calcRect(map[minI][minJ], map[minI][maxJ], map[maxI][minJ], map[maxI][maxJ],
    (1 - ratioA) * (1 - ratioB), (1 - ratioA) * ratioB, ratioA * (1 - ratioB), ratioA * ratioB);
}

